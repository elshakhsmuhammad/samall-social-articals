     */
    public function store(Request $request)
    {

        if ($request->ajax()) {
            $user = Auth::user();
            $post = new Post;

            $post->user_id = $user->id;
            $post->title = $request->input('title');
            $post->body = $request->input('body');

            $post->save();

            return response($post);
        }
}





